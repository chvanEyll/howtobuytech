	<h2>Comparer des routeurs</h2>
		<p>
			Comparer des routeurs entre eux peut s'avérer fastidieux de par les termes techniques employés pour les différencier. 
			La liste suivante présente les caractéristiques en ordre d'importance afin de vous aider dans votre choix, mais aussi 
			afin que vous cerniez celles qui vous importent.
		</p>
		<p>
			<b>Vitesse:</b> <br>
			La vitesse d'un routeur est souvent sa caractéristique la mieux indiquée et s'exprime habituellement sous la forme 300Mbps, 450Mbps etc.
			Il importe de savoir que ce chiffre n'influence pas la vitesse de votre connexion Internet, mais celle du réseau sans-fil généré par le routeur.
		</p>
		<p>
			La norme actuelle (300Mbps) est considérée comme étant largement suffisante à une utilisation domestique.
		</p>
		<p>
			<b>Nombre de bandes:</b> <br>
			Les routeurs simple-bande projettant une transmission 2.4Ghz (plus grande portée), les routeurs double-bande peuvent aider à alléger un réseau
			grâce à leur seconde transmission 5Ghz (plus rapide et moins d'interférences). Les bandes apparaîtront toutes deux comme un réseau sans-fil à
			part entière.
		</p>
		<p>
			<b>Port USB:</b> <br>
			Les routeurs munis d'un port USB permettent d'y brancher une imprimante ou un disque dur externe dans le but de le rendre disponible à tous
			les appareils du réseau.
		</p>
		<p>
			<b>Norme:</b> <br>
			Les termes 802.11n, 802.11b ou 802.11ac correspondent à des normes de connexion. Si le 802.11b est maintenant dépassé, les routeurs actuels
			fonctionnent à peu près tous sur le 802.11n. Le 802.11ac, quant à lui, est une nouvelle norme qui, pour l'instant, en est encore au stade de
			développement et n'est donc pas encore compatible avec la majorité des appareils.
			
			Les termes WEP, WPA et WPA2 représentent eux des normes de sécurité et ne sont donc pas nécessairement à prendre en considération puisque les
			routeurs actuels utilisent presque tous le WPA2.
		</p>
	<h2>Acheter un routeur</h2>
		<p>
			Puisque les magasins grande surface égalent habituellement le prix des routeurs sur présentation d'une preuve de disponibilité en ligne incluant
			les frais de transport, l'achat en ligne ne représente pas nécessairement une économie. Un routeur 300Mbps simple-bande coûtera entre 30 et 50$, 
			alors qu'un routeur 600Mbps double-bande peut coûter entre 60 et 100$.
		</p>
		<p>
			Finalement, suite à l'achat d'un routeur, il importe d'en effectuer la <a href="#">configuration et la sécurisation</a>.
		</p>
