		<p>
			Pour comparer des ordinateurs de jeux vidéo entre eux, il faut définir votre besoin exact à l'aide des spécifications
			disponibles sur le site du fabricant du jeu auquel vous envisagez jouer. Il importe de garder en tête que les jeux vidéo évoluent
			beaucoup et qu'il existe une forte différence entre les spécifications minimales et maximales de certains jeux.
		</p>	
		<p>
			La liste suivante est à titre introductif; il existe de nombreux 
			<a href="http://www.build-gaming-computers.com/gaming-desktop-computer.html">forums</a> dédiés à la conception d'ordinateurs de jeux vidéo
		</p>
		<p>
			<b>Processeur:</b> <br>
			Bien que la puissance du processeur se mesure à l'aide d'<a href="http://www.cpubenchmark.net/">indices de référence</a>, les processeurs
			d'origine dans les ordinateurs actuels sont habituellement suffisants.
		</p>
		<p>
			<b>Carte graphique:</b> <br>
			La carte graphique est l'élément le plus important pour un ordinateur de jeu. Il peut être très intéressant de comparer leur prix et leur 
			<a href="http://www.videocardbenchmark.net/">indice de référence</a> afin de trouver la meilleure, une fois les spécifications désirées établies.
		</p>
		<p>
			<b>Mémoire vive:</b> <br>
			Les recommandations habituelles de mémoire vive pour les jeux vidéo oscillent entre 4 et 8Go.
		</p>
		<p>
			<b>Espace disque:</b> <br>
			L'espace disque n'est pas un problème; par contre, l'ajout d'un SSD peut être intéressant pour certains. Plus d'infos <a href="#">ici</a>.
		</p>
	<h2>Acheter un ordinateur de jeux vidéo</h2>
		<p>
			Bien qu'il soit possible d'acheter un ordinateur suffisamment puissant en magasin, il existe de nombreuses 
