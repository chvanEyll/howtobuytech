	<h2>Comparer des ordinateurs de bureautique</h2>
		<p>
			Afin de comparer des ordinateurs de bureau et trouver celui qui répondra à vos besoins,
			il importe de préciser les logiciels que vous comptez utiliser. Par exemple, si vous comptez 
			utiliser la suite Office et un navigateur web, la grande majorité des ordinateurs de bureau 
			actuels sauront aisément répondre au besoin.
		</p>
		<p>
			Par contre, si vous aurez à utiliser des logiciels spécifiques, il vaut la peine de vérifier 
			la puissance recommandée sur le site web du fabricant en gardant en tête que les logiciels évoluent
			et que l'on en utilise souvent plusieurs à la fois.
		</p>
		<p>
			<b>Processeur:</b> <br>
			Mesurable selon des <a href="http://www.cpubenchmark.net/">indices de référence</a>, 
			mais les processeurs des ordinateurs actuels conviennent tous à une utilisation bureautique.
		</p>
		<p>
			<b>Mémoire vive:</b> <br>
			Le minimum habituel est de 4Go, ce qui est amplement suffisant pour une utilisation bureautique.
		</p>
		<p>	
			<b>Espace disque:</b> <br>
			Le minimum habituel, 500Go, contiendra des millers de photos et des centaines de films. <br>
			L'ajout d'un SSD peut être pertinent; plus d'informations ainsi qu'un guide d'installation <a href="#">ici</a>.
		</p>
	<h2>Acheter un ordinateur de bureautique</h2>
		<p>
			Les magasins d'informatique offrant souvent des prix compétitifs, acheter en ligne ne représente pas
			nécessairement une économie. Par ailleurs, il est à noter que les magasins égalent habituellement les
			prix en ligne sur présentation d'une preuve de la disponibilité du produit incluant les frais de transport.
			Le prix d'un ordinateur de bureautique convenable commence autour de 400$ sans compter le <a href="moniteurs.php">moniteur</a>.
		</p>
		<p>
			Finalement, suite à l'achat de tout ordinateur, il importe de <a href="#">créer une sauvegarde initiale</a>
			et d'<a href="#">optimiser l'ordinateur</a>.
		</p>

<a name="modelisation">
	<img alt="Ordinateurs de modélisation 3D" id="mobile" src="images/acad-banniere-mobile.jpg">
	<img alt="Ordinateurs de modélisation 3D" id="widescreen" src="images/acad-banniere.jpg">
</a>
	<h2>Comparer des ordinateurs de modélisation 3D</h2>
		<p>
			Comparer des ordinateurs de modélisation 3D nécessite de vérifier les spécifications requises sur le
			site du fabricant du logiciel que vous utiliserez, en gardant en tête que les logiciels évoluent et que
			l'on en utilise souvent plus d'un à la fois.
		</p>	
		<p>
			La liste suivante est à titre introductif; il existe de nombreux 
			<a href="http://www.tomshardware.com/forum/forum-31.html">forums</a> dédiés à la conception de stations
			de travail.
		</p>
		<p>
			<b>Processeur:</b> <br>
			Le processeur est le principal responsable de la vitesse du rendu et se mesure par <a href="http://www.cpubenchmark.net/">indices de référence</a>.
			On recommande généralement d'opter pour un processeur Intel i7.
		</p>
		<p>
			<b>Carte graphique:</b> <br>
			Bien qu'une carte graphique spécialisée pour la modélisation soit préférable, leur prix plus élevé fait en sorte qu'une carte graphique de jeu
			est une alternative acceptable. Les cartes graphiques se mesurent elles aussi à l'aide d'
			<a href="http://www.videocardbenchmark.net/">indices de référence</a> et augmentent la vitesse d'affichage dans l'écran de visualisation.
		</p>
		<p>
			<b>Mémoire vive:</b> <br>
			La mémoire vive recommandée varie entre 8 et 64Go; elle n'accélère pas le processus de rendu mais l'ordinateur est à risque de planter s'il en 
			vient à court.
		</p>
		<p>
			<b>Espace disque:</b> <br>
			L'espace disque n'est pas un problème; par contre, l'ajout d'un SSD est conseillé. Plus d'infos <a href="#">ici</a>.
		</p>
	<h2>Acheter un ordinateur de modélisation 3D</h2>
		<p>
			Bien qu'il soit possible d'acheter un ordinateur suffisamment puissant en magasin, il existe de nombreuses 
			<a href="http://www.reddit.com/r/buildapc/comments/1yg8xg/dos_donts_and_tips_for_first_time_bulders/">ressources en ligne</a>
			au sujet des configurations personnalisées. Il importe alors de se documenter suffisamment
			pour être sûr de se procurer une carte mère compatible et une alimentation suffisante, par exemple.
			Un ordinateur de modélisation 3D convenable commence autour de 700$ sans compter le <a href="moniteurs.php>moniteur</a>.
		</p>
		<p>
			Finalement, suite à l'achat de tout ordinateur, il importe de <a href="#">créer une sauvegarde initiale</a>
			et d'<a href="#">optimiser l'ordinateur</a>.
		</p>
