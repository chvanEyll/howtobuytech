			au sujet des configurations personnalisées. Une option fréquente consiste en l'achat d'un ordinateur complet en magasin et d'une 
			carte graphique plus performante en ligne. Il importe alors de se documenter suffisamment pour être sûr de se procurer une carte 
			mère compatible et une alimentation suffisante, par exemple. Un ordinateur de jeu convenable peut coûter aussi peu que 400$ plus
