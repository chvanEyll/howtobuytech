	<h2>Comparer des portables de bureautique</h2>
		<p>
			Afin de comparer des portables et trouver celui qui répondra à vos besoins,
			il importe de préciser les logiciels que vous comptez utiliser. Par exemple, si vous comptez 
			utiliser la suite Office et un navigateur web, la plupart des portables conviendront au besoin.
		</p>
		<p>
			Par contre, si vous aurez à utiliser des logiciels spécifiques, il vaut la peine de vérifier 
			la puissance recommandée sur le site web du fabricant en gardant en tête que les logiciels évoluent
			et que l'on en utilise souvent plusieurs à la fois.
		</p>
		<p>
			<b>Processeur:</b> <br>
			Mesurable selon des <a href="http://www.cpubenchmark.net/">indices de référence</a>; 
			Pour un portable, on recommande généralement un Intel i3, i5 ou similaire pour pallier à
			l'évolution des logiciels.
		</p>
		<p>
			<b>Mémoire vive:</b> <br>
			Le minimum habituel est de 4Go, ce qui est amplement suffisant pour une utilisation bureautique.
		</p>
		<p>	
			<b>Espace disque:</b> <br>
			Le minimum habituel, 500Go, contiendra des millers de photos et des centaines de films.
			Les Ultrabook (de nouveaux portables légers, minces et puissants) viennent souvent avec des disques durs SSD;
			il faut alors faire attention à l'espace disque parfois très limité (64Go ne contiendra pas beaucoup plus que le
			système d'exploitation de l'ordinateur). Plus d'informations au sujet des SSD <a href="#">ici</a>.
		</p>
	<h2>Acheter un portable de bureautique</h2>
		<p>
			Les magasins d'informatique offrant souvent des prix compétitifs, acheter en ligne ne représente pas
			nécessairement une économie. Par ailleurs, il est à noter que les magasins égalent habituellement les
			prix en ligne sur présentation d'une preuve que le produit est disponible et incluant les frais de transport.
			Un portable de bureautique devrait coûter 400$ sauf s'il s'agit d'un Ultrabook.
		</p>
		<p>
			Finalement, suite à l'achat de tout ordinateur, il importe de <a href="#">créer une sauvegarde initiale</a>
			et d'<a href="#">optimiser l'ordinateur</a>.
		</p>

<a name="modelisation">
	<img alt="Portables de modélisation" id="mobile" src="images/acad-banniere-mobile.jpg">
