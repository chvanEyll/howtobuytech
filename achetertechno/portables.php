	<h2>Comparer des portables de modélisation 3D</h2>
		<p>
			Comparer des ordinateurs de modélisation 3D nécessite de vérifier les spécifications requises sur le
			site du fabricant du logiciel que vous utiliserez, en gardant en tête que les logiciels évoluent et que
			l'on en utilise plus d'un à la fois; cependant, il est à considérer que les portables auront un rapport
			performance graphique/prix bien inférieur aux ordinateurs de bureau.
		</p>	
		<p>
			La liste suivante est à titre introductif; il existe de nombreux 
			<a href="http://www.tomshardware.com/forum/forum-35.html">forums</a> dédiés à la comparaison de portables
			de performance.
		</p>
		<p>
			<b>Processeur:</b> <br>
			Le processeur est le principal responsable de la vitesse du rendu et se mesure par <a href="http://www.cpubenchmark.net/">indices de référence</a>.
			On recommande généralement d'opter pour un processeur Intel i7.
		</p>
		<p>
			<b>Carte graphique:</b> <br>
			Bien qu'une carte graphique spécialisée pour la modélisation soit préférable, leur prix plus élevé fait en sorte qu'une carte graphique de jeu
			est une alternative acceptable. Les cartes graphiques se mesurent elles aussi à l'aide d'
			<a href="http://www.videocardbenchmark.net/">indices de référence</a> et augmentent la vitesse d'affichage dans l'écran de visualisation.
		</p>
		<p>
			<b>Mémoire vive:</b> <br>
			La mémoire vive recommandée varie entre 8 et 16Go; elle n'accélère pas le processus de rendu mais l'ordinateur est à risque de planter s'il en 
			vient à court.
		</p>
		<p>
			<b>Espace disque:</b> <br>
			S'il s'agit d'un portable régulier, l'espace disque ne posera pas problème. L'<a href="#">ajout d'un SSD</a> peut être pertinent pour accélérer l'ouverture de 
			certains logiciels. Par contre, certains Ultrabook ne sont dotés que d'un SSD de 64Go; il est alors préférable d'opter pour un Ultrabook avec un SSD de 128Go au moins.
		</p>
	<h2>Acheter un portable de modélisation 3D</h2>
		<p>
			Puisqu'il existe de nombreuses ressources en ligne concernant la comparaison de portables de performance, il peut être intéressant
			de se le procurer en ligne puisqu'il est alors possible d'acheter précisément ce dont nous avons besoin. Toutefois, certains portables en magasin
			(souvent des portables de jeu) peuvent convenir pour la modélisation. Le prix d'un portable de modélisation se situe habituellement entre 1000
			et 1500$.
		</p>
		<p>
			Finalement, suite à l'achat de tout ordinateur, il importe de <a href="#">créer une sauvegarde initiale</a>
			et d'<a href="#">optimiser l'ordinateur</a>.
		</p>
