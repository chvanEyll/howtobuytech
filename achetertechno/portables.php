	<h2>Comparer des portables de jeux vidéo</h2>
		<p>
			Pour comparer des portables de jeux vidéo entre eux, il faut définir votre besoin exact à l'aide des spécifications
			disponibles sur le site du fabricant du jeu auquel vous envisagez jouer. Il importe de garder en tête que les jeux vidéo évoluent
			beaucoup et qu'il existe une forte différence entre les spécifications minimales et maximales de certains jeux. Par ailleurs, il
			est à noter que les portables ont un rapport performance graphique/prix bien inférieur aux ordinateurs de bureau, et qu'ils ont tendance à
			chauffer lorsque trop fortement sollicités.
		</p>	
		<p>
			La liste suivante est à titre introductif; il existe de nombreux 
			<a href="http://www.tomshardware.com/forum/forum-35.html">forums</a> dédiés à la comparaison de portables
			de performance.
		</p>
		<p>
			<b>Processeur:</b> <br>
			La puissance du processeur se mesure à l'aide d'<a href="http://www.cpubenchmark.net/">indices de référence</a>. Pour le jeu, on recommande
			habituellement un Intel i5 ou i7.
		</p>
		<p>
			<b>Carte graphique:</b> <br>
			La carte graphique est l'élément le plus important pour un portable de jeu. Il peut être très intéressant de comparer leur prix et leur 
			<a href="http://www.videocardbenchmark.net/">indice de référence</a> afin de trouver la meilleure, une fois les spécifications désirées établies.
		</p>
		<p>
			<b>Mémoire vive:</b> <br>
			Les recommandations habituelles de mémoire vive pour les jeux vidéo oscillent entre 4 et 8Go.
		</p>
		<p>
			<b>Espace disque:</b> <br>
			L'espace disque n'est pas un problème; par contre, l'ajout d'un SSD peut être intéressant pour certains. Plus d'infos <a href="#">ici</a>.
		</p>
	<h2>Acheter un portable de jeux vidéo</h2>
		<p>
			Puisqu'il existe de nombreuses ressources en ligne concernant la comparaison de portables de performance, il peut être intéressant
			de se le procurer en ligne puisqu'il est alors possible d'acheter précisément ce dont nous avons besoin. Toutefois, certains modèles
			sont vendus en magasin et sont assez intéressants. Le prix d'un portable de jeu se situe habituellement entre 1000
			et 1500$.
		</p>
		<p>
