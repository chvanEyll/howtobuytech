	<h2>Comparing printers</h2>
		<p>
			<b>Functions:</b> <br>
			While inkjet printers usually come with photocopy and colors, those options greatly influence the price of laser printers. Since those offer a 
			better cost per page, it is important to specify wether colors and/or photocopy are options you really need.
		</p>
		<p>
			<b>Cost per page:</b> <br>
			While some printers cost the same as their cartridge, stock cartridges that come with the printer are often filled at a third of their capacity. 
			Therefore, cost per page is one of the most important specifications, and you can often find it on the box or the company's website.
		</p>
		<p>
			<b>Connectivity:</b> <br>
			While printers are traditionnally connected via USB, some more recent models can connect to the WiFi so that all devices on the network 
			can communicate with the printer. The connection can be done with either an Ethernet cable, a USB cable (if the router has a USB plug) or through WiFi.
		</p>
		<p>
			It is also important to differentiate ePrint and AirPrint; while AirPrint is an Apple technology which allows iPads, iPods and iPhones to send a print 
			request through the WiFi without any driver needed, ePrint allows a device to communicate to your printer from anywhere as long as you are connected to 
			the Internet.
		</p>
	<h2>Buying a printer</h2>
		<p>
			While in-store rebates can sometimes be very competitive in addition to stores often matching online prices, cartridges and accessories are often way cheaper 
			online. 
		</p>
		<p>
			A multifunction inkjet printer with a good cost per page may cost between 100 et 150$, a monochrome laser printer can cost under 100$, adding the multifunction 
			will add 50-100$ to the price and the colors as well.
