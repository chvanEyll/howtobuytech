<h1>Monitors</h1>

	<h2>Comparing monitors</h2>
		<p>
			Comparing monitors require that you determine the intended use, be it office use, graphic design or gaming. It is then important to consider the following
			elements, sorted by importance.
		</p>
		<p>
			<b>Size:</b> <br>
			Recent monitors are almost all widescreen and their size usually vary between 20 and 30 inches, with an important price difference between both; however, 
			there are bigger screen sizes on the market. Choosing a size also requires that you determine wether you intend to use more than one monitor on your computer.
		</p>
		<p>
			<b>Resolution:</b> <br>
			The usual monitor resolution is 1920x1080px (Full HD). However, 27in screens and more can benefit from a 2560x1440px resolution, which increases the 
			price of the monitor of 100$ and more.
		</p>
		<p>	
			<b>IPS technology:</b> <br>
			IPS technology improves the color rendering significantly. Graphic designers and gamers can both benefit from an IPS monitor, even though it increases the 
			price of the monitor of about 70$.
		</p>
		<p>	
			<b>Response time:</b> <br>
			Response time is expressed in milliseconds (ms) and is an important factor for First-Person Shooters or other fast games. Such games will benefit from a 
			2ms response time, while the average is around 5ms.
		</p>
		<p>	
			<b>Refresh rate:</b> <br>
			Refresh rate is measured in Hertz. The average is around 60Hz and a higher one is only useful for users of 3D screens.
		</p>
		<p>	
			<b>Contrast:</b> <br>
			Since companies do not use the same scales to measure contrast ratios, it is not an important comparative element.
		</p>
	<h2>Purchasing a monitor</h2>
		<p>
			Since retail stores usually match online prices when shown a proof of availability including shipping costs, buying online is not always the cheapest option. 
			A 20in monitor will cost around 100$ while a 27in screen with a 2540x1440px resolution can cost around 350$.
