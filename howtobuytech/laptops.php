			500GB is the usual minimum and it will contain thousands of photos and hundreds of movies. However, Ultrabooks often 
			come with SSDs; it is then important to check the storage capacitiy, as 64GB SSDs will not hold a lot more than the 
			operating system. More info on SSDs <a href="#">here</a>.
		</p>
