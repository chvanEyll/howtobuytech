	<h2>Comparing 3D modeling laptops</h2>
		<p>
			In order to compare 3d modeling desktop computers, it is important to verify the specified requirements for 
			the exact software that you intend to use on the software website, keeping in mind that software tends to get 
			heavier with time and that you might want to use more than one at the same time. <br><br>
			Also, laptops have a lower graphic performance/price ratio than desktops.
		</p>	
		<p>
			The following list is introductory; there are numerous <a href="http://www.tomshardware.com/forum/forum-35.html">forums</a> 
			out there that are entirely dedicated to comparing performance laptops.
		</p>
		<p>
			<b>Processor:</b> <br>
			The processor is mainly responsible for rendering speed and its performance is measured using
			<a href="http://www.cpubenchmark.net/">benchmarks</a>. An Intel i7 processor is usually recommended for 3D modeling.
		</p>
		<p>
			<b>Graphic card:</b> <br>
			While a modeling graphic card is recommended, many people will opt for a gaming card because of their lower price. Graphic cards are also 
			compared using <a href="http://www.videocardbenchmark.net/">benchmarks</a> and they help the viewport display speed.
		</p>
		<p>
			<b>Ram:</b> <br>
			The recommended amount of ram varies between 8 and 16GB; while it does not accelerate the rendering process, the computer may crash if it runs 
			out of it.
		</p>
		<p>
			<b>Disk space:</b> <br>
			Some Ultrabooks may face a storage problem due to the storage capacity of their SSDs. However, if the laptop is a regular one, the disk space will 
			not be a problem and <a href="#">adding an SSD</a> may help with heavier software.
		</p>
	<h2>Purchasing a 3D modeling laptop</h2>
		<p>
			Buying your modeling laptop online is often the best option considering the wide choice of available configurations. However, some in-store latops 
