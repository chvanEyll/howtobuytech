	<h2>Comparing routers</h2>
		<p>
			Comparing routers may be daunting considering the numerous technical terms employed to differentiate them. The following list presents 
			the important specifications to look for in order to help your purchasing process.
		</p>
		<p>
			<b>Speed:</b> <br>
			A router's speed is often the easiest specification to find and is usually expressed in Mbps. While 300Mbps is the average, this number does not 
			influence your connection speed; it only accelerates the network speed. In other words, 300Mbps is perfectly fine for a single-band router.
		</p>
		<p>
			<b>Number of bands:</b> <br>
			While single band routers only have a 2.4Ghz band (broader range), dual band routers may help your network speed with their 5Ghz band (faster and 
			less interferences). Both bands will appear as a separate network to connect to.
		</p>
		<p>
			<b>USB port:</b> <br>
			Routers with an USB port allow you to plug a printer or a hard disk in it in order to make it available to every device on the network.
		</p>
		<p>
			<b>Standard:</b> <br>
			802.11n, 802.11b and 802.11ac all are wireless standards. While 802.11b is not obsolete, almost all recent routers work on the 802.11n. 802.11ac 
			is a new standard which is still in developement state, therefore it is not compatible with most devices.<br><br>
			
			WEP, WPA, WPA2 are all security standards which are not to be taken into consideration considering that most recent routers use WPA2.
		</p>
